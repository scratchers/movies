    {
        View::share('create', [
            'id'    => 'link-create-tag',
            'class' => Tag::class,
            'route' => route('tags.create'),
        ]);
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
